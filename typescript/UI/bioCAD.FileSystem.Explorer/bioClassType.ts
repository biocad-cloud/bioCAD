enum bioClassType {
    /**
     * The unknown class type
    */
    unknown = 0,
    /**
     * General text file
    */
    text = 1,
    /**
     * Image file
    */
    image = 2,
    /**
     * The data table is a kind of numeric matrix for gene expression data, or something.
    */
    matrix = 3,
    /**
     * The biological sequence data type, like fasta sequence file.
    */
    bioSequence = 4
}